package org.go.spring.angel.base.to;

public class CodeDetailBean {

	private String codeNo;		//기본코드
	private String codeDetailNo;		//상세코드
	private String codeDetailName;		//상세코드명

	public String getCodeNo() {
		return codeNo;
	}
	public void setCodeNo(String codeNo) {
		this.codeNo = codeNo;
	}
	public String getCodeDetailNo() {
		return codeDetailNo;
	}
	public void setCodeDetailNo(String codeDetailNo) {
		this.codeDetailNo = codeDetailNo;
	}
	public String getCodeDetailName() {
		return codeDetailName;
	}
	public void setCodeDetailName(String codeDetailName) {
		this.codeDetailName = codeDetailName;
	}


}
